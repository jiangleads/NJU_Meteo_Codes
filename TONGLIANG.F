	SUBROUTINE TONGLIANG(L,M,N,U,V,OMEGA,Q,QX,QY,QP)
	REAL(4),DIMENSION(L,M,N)::U,V,OMEGA,Q,QX,QY,QP
	REAL(4),PARAMETER::G=9.8
	DO I=1,L
	  DO J=1,M
	    DO K=1,N
		  QX(I,J,K)=Q(I,J,K)*U(I,J,K)/G
		  QY(I,J,K)=Q(I,J,K)*V(I,J,K)/G
		  QP(I,J,K)=Q(I,J,K)*OMEGA(I,J,K)/G
	    END DO
	  END DO
	END DO
	END
